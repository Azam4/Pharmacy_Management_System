public class MedicineModel {
    
    private String m_id;
    private String m_name;
    private int m_price;
    private int m_qty;
    private String m_fabdate;
    private String m_expdate;
    private String m_comp;

   

    public String getM_id() {
        return m_id;
    }

    public void setM_id(String m_id) {
        this.m_id = m_id;
    }

    public String getM_name() {
        return m_name;
    }

    public void setM_name(String m_name) {
        this.m_name = m_name;
    }

    public int getM_price() {
        return m_price;
    }

    public void setM_price(int m_price) {
        this.m_price = m_price;
    }

    public int getM_qty() {
        return m_qty;
    }

    public void setM_qty(int m_qty) {
        this.m_qty = m_qty;
    }

    public String getM_fabdate() {
        return m_fabdate;
    }

    public void setM_fabdate(String m_fabdate) {
        this.m_fabdate = m_fabdate;
    }

    public String getM_expdate() {
        return m_expdate;
    }

    public void setM_expdate(String m_expdate) {
        this.m_expdate = m_expdate;
    }

    public String getM_comp() {
        return m_comp;
    }

    public void setM_comp(String m_comp) {
        this.m_comp = m_comp;
    }
    
}
